# ================== دوال البوت ==================
def generate_quote():
    personality = random.choice(PERSONALITIES)
    print(f"👤 الشخصية المختارة: {personality}")
    
    client = Groq(api_key=GROQ_API_KEY)
    
    prompt = f"""اكتب اقتباساً أو حكمة أو خاطرة أو بيت شعر منسوباً إلى {personality}.
اكتب الاقتباس فقط دون مقدمة أو شرح.
في نهاية الاقتباس، اكتب اسم الشخصية بين قوسين مع ذكر مجالها.
لا تذكر كلمة "اقتباس" أو "خاطرة" في البداية."""
    
    try:
        chat_completion = client.chat.completions.create(
            messages=[
                {"role": "system", "content": "أنت بوت أدبي متخصص بالحكم والاقتباسات."},
                {"role": "user", "content": prompt}
            ],
            model="llama-3.3-70b-versatile",
            temperature=0.9,
            max_tokens=150,
        )
        quote = chat_completion.choices[0].message.content.strip()
        print(f"📄 رد Groq: {quote[:50]}...")
        return quote
            
    except Exception as e:
        print(f"❌ خطأ في التوليد: {e}")
        return '"العلم نور والجهل ظلمة" (الإمام علي)'

def send_quote():
    quote = generate_quote()
    url = f"https://api.telegram.org/bot{TOKEN}/sendMessage"
    text = f"{quote}\n\n#جمهورية_الزلم"
    data = {"chat_id": CHANNEL_ID, "text": text, "parse_mode": "HTML"}
    
    try:
        response = requests.post(url, data=data, timeout=10)
        if response.status_code == 200:
            print(f"✅ تم الإرسال: {quote[:30]}...")
        else:
            print(f"❌ فشل الإرسال: {response.status_code}")
    except Exception as e:
        print(f"❌ خطأ: {e}")

def send_message(chat_id, text):
    url = f"https://api.telegram.org/bot{TOKEN}/sendMessage"
    data = {"chat_id": chat_id, "text": text, "parse_mode": "HTML"}
    try:
        requests.post(url, data=data, timeout=10)
    except Exception as e:
        print(f"❌ خطأ في الإرسال: {e}")

def handle_message(chat_id, text):
    if text == "/start":
        send_message(chat_id, "👋 أهلاً بك! أنا بوت الاقتباسات الأدبية.\n\n📌 أرسل لي أي شيء وسأرد عليك بأفضل ما عندي.")
    else:
        send_message(chat_id, f"📝 شكراً لرسالتك! سأرد عليك قريباً.\n\n💬 رسالتك: {text[:50]}...")

def main():
    print("=" * 50)
    print("📚 بوت الاقتباسات الأدبية شغال!")
    print("=" * 50)
    print(f"📢 القناة: {CHANNEL_ID}")
    print(f"👤 عدد الشخصيات: {len(PERSONALITIES)}")
    print("🤖 يستخدم Groq API (مجاني وسريع)")
    print("⏰ سيرسل كل 10 دقائق")
    print("💬 يرد على رسائل المستخدمين")
    print("=" * 50)
    print("✅ اضغط Ctrl+C للإيقاف")
    print("=" * 50)
    
    last_send = datetime.now()
    last_update = 0
    
    while True:
        now = datetime.now()
        
        if (now - last_send).seconds >= 600:
            send_quote()
            last_send = now
        
        try:
            url = f"https://api.telegram.org/bot{TOKEN}/getUpdates"
            params = {"offset": last_update + 1, "timeout": 30}
            response = requests.get(url, params=params, timeout=35)
            updates = response.json().get("result", [])
            
            for update in updates:
                last_update = update["update_id"]
                if "message" in update:
                    msg = update["message"]
                    chat_id = msg["chat"]["id"]
                    text = msg.get("text", "")
                    if str(chat_id) != str(CHANNEL_ID):
                        handle_message(chat_id, text)
        except Exception as e:
            print(f"❌ خطأ في جلب الرسائل: {e}")
        
        time.sleep(5)

if __name__ == "__main__":
    main()
